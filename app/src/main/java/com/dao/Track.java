package com.dao;

public class Track {
	private String 节点;
	private	String 经办人;
	private	String 时间;
	private	String Lon;
	private	String Lat;
	private	String 公司;
	private	String 备注;

	public String get节点() {
		return 节点;
	}
	public void set节点(String 节点) {
		this.节点 = 节点;
	}
	public String get经办人() {
		return 经办人;
	}
	public void set经办人(String 经办人) {
		this.经办人 = 经办人;
	}
	public String get时间() {
		return 时间;
	}
	public void set时间(String 时间) {
		this.时间 = 时间;
	}
	public String getLon() {
		return Lon;
	}
	public void setLon(String lon) {
		Lon = lon;
	}
	public String getLat() {
		return Lat;
	}
	public void setLat(String lat) {
		Lat = lat;
	}
	public String get公司() {
		return 公司;
	}
	public void set公司(String 公司) {
		this.公司 = 公司;
	}
	public String get备注() {
		return 备注;
	}
	public void set备注(String 备注) {
		this.备注 = 备注;
	}




}
