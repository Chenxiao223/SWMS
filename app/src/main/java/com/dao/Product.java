package com.dao;

public class Product {

	private int order;
	private	String 产品名称;
	private	String 产品广告;
	private	String 介绍;


	public int getOrder() {
		return order;
	}
	public void setOrder(int order) {
		this.order = order;
	}
	public String get产品名称() {
		return 产品名称;
	}
	public void set产品名称(String 产品名称) {
		this.产品名称 = 产品名称;
	}
	public String get产品广告() {
		return 产品广告;
	}
	public void set产品广告(String 产品广告) {
		this.产品广告 = 产品广告;
	}
	public String get介绍() {
		return 介绍;
	}
	public void set介绍(String 介绍) {
		this.介绍 = 介绍;
	}


}
