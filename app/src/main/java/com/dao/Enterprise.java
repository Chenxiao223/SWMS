package com.dao;

public class Enterprise {
	private int order;
	private	String 企业名称;
	private	String 企业广告;
	private	String 官网;
	private	String 简介;

	public int getOrder() {
		return order;
	}
	public void setOrder(int order) {
		this.order = order;
	}
	public String get企业名称() {
		return 企业名称;
	}
	public void set企业名称(String 企业名称) {
		this.企业名称 = 企业名称;
	}
	public String get企业广告() {
		return 企业广告;
	}
	public void set企业广告(String 企业广告) {
		this.企业广告 = 企业广告;
	}
	public String get官网() {
		return 官网;
	}
	public void set官网(String 官网) {
		this.官网 = 官网;
	}
	public String get简介() {
		return 简介;
	}
	public void set简介(String 简介) {
		this.简介 = 简介;
	}
	
}
